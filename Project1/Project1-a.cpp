static inline uint32_t move(uint32_t x, int n) { //左循环移位
    return (x << n) | (x >> (32 - n));
}
static inline uint32_t map(uint32_t n) { //线性变换
    return n^move(n,2)^move(n,10)^move(n,18)^move(n,24);
}
static uint32_t T[256]; //全局T-Table
void init_T() { //初始化T-table
    for (int i = 0; i < 256; i++) {
        uint32_t b = Sbox[i]; //合并计算，避免重复
        T[i] = map((b << 24) | (b << 16) | (b << 8) | b);
    }
}
static inline uint32_t noliner(uint32_t a) { //非线性变换
    uint8_t a0 = (a >> 24) & 0xFF, a1 = (a >> 16) & 0xFF, a2 = (a >> 8) & 0xFF, a3 = a & 0xFF;
    return (uint32_t)Sbox[a0] << 24 | (uint32_t)Sbox[a1] << 16
        | (uint32_t)Sbox[a2] << 8 | (uint32_t)Sbox[a3];
}

void expand(const uint8_t key[16], uint32_t rk[32]) { //密钥扩展
    uint32_t K[36];
    for (int i = 0; i < 4; i++) {
        K[i] = ((uint32_t)key[4 * i] << 24) | ((uint32_t)key[4 * i + 1] << 16)
            | ((uint32_t)key[4 * i + 2] << 8) | ((uint32_t)key[4 * i + 3]);
        K[i] ^= FK[i];
    }
    for (int i = 0; i < 32; i++) { //生成32轮子密钥
        uint32_t tmp = K[i + 1] ^ K[i + 2] ^ K[i + 3] ^ CK[i];
        uint32_t buf = noliner(tmp);
        buf = buf ^ move(buf, 13) ^ move(buf, 23);
        rk[i] = K[i] ^ buf;//迭代生成rk
        K[i + 4] = rk[i];
    }
}

void encrypt(const uint8_t in[16], uint8_t out[16], const uint32_t rk[32]) {
    uint32_t res[36]; //加密函数，输出密文
    for (int i = 0; i < 4; i++) {
        res[i] = ((uint32_t)in[4 * i] << 24) | ((uint32_t)in[4 * i + 1] << 16)
            | ((uint32_t)in[4 * i + 2] << 8) | ((uint32_t)in[4 * i + 3]);
    }
    for (int i = 0; i < 32; i++) { //使用 T-Table 加速32轮迭代
        uint32_t tmp = res[i + 1] ^ res[i + 2] ^ res[i + 3] ^ rk[i];
        res[i + 4] = res[i] ^ T[(tmp >> 24) & 0xFF]
            ^ T[(tmp >> 16) & 0xFF]
            ^ T[(tmp >> 8) & 0xFF]
            ^ T[(tmp) & 0xFF];
    }
    for (int i = 0; i < 4; i++) { //逆序输出
        uint32_t x = res[35 - i];
        out[4 * i] = (x >> 24) & 0xFF;
        out[4 * i + 1] = (x >> 16) & 0xFF;
        out[4 * i + 2] = (x >> 8) & 0xFF;
        out[4 * i + 3] = x & 0xFF;
    }
}
int main() 
{
    init_T();
    uint32_t rk[32];
    expand(MK, rk); //生成子密钥
    uint8_t IV[16]; //随机生成IV
    random_device rd;
    mt19937 gen(rd());
    uniform_int_distribution<int> dist(0, 255);
    for (int i = 0; i < 16; i++) {
        IV[i] = static_cast<uint8_t>(dist(gen));
    }
    string input;
    input = "SDUzxj"; //测试明文
    size_t orig_len = input.size(); // PKCS#7 填充
    size_t pad_len = 16 - (orig_len % 16); //填充长度计算
    vector<uint8_t> buffer(orig_len + pad_len);
    memcpy(buffer.data(), input.data(), orig_len);
    memset(buffer.data() + orig_len, (uint8_t)pad_len, pad_len);
    //CBC模式加密
    vector<uint8_t> cipher(buffer.size());
    uint8_t IV_pre[16];
    memcpy(IV_pre, IV, 16); //存储IV与前一密文
    LARGE_INTEGER Freq; //API高精度计时器，微秒级
    LARGE_INTEGER Start;
    LARGE_INTEGER End;
    QueryPerformanceFrequency(&Freq);
    QueryPerformanceCounter(&Start);
    for (size_t offset = 0; offset < buffer.size(); offset += 16) {
        uint8_t block[16];
        //异或上一个密文块或IV
        for (int i = 0; i < 16; i++) {
            block[i] = buffer[offset + i] ^ IV_pre[i];
        }
        encrypt(block, &cipher[offset], rk); //主加密
        memcpy(IV_pre, &cipher[offset], 16); //更新前一块密文
    }
    QueryPerformanceCounter(&End);
    cout << "CBC模式SM4加密结果为：";
    for (auto b : cipher) { // 输出全部密文（hex）
        cout << hex << setw(2) << setfill('0')
            << (int)b;
    }
    cout << endl;
    double time = (((End.QuadPart - Start.QuadPart) * 1000.0) / Freq.QuadPart);
    cout << "加密用时:" << time << "ms" << endl;
    return 0;
}